// The sort() sorts the elements as strings in alphabetical and ascending order.
// change original array

let age1 = [33, 55, 67, 18];
let s_age = age1.sort();

console.log(s_age, age1); // both are same
// [ 18, 33, 55, 67 ] [ 18, 33, 55, 67 ]

